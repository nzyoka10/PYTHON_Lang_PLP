''' 
== Equal to
!= Not equal to
< Less than
> Greater than
<= Less than or equal to
>= Greater than or equal to
'''

a = 5
b = 7

print("\n a = 5 and b = 7 ")

print('\n Is {a == b}: ',  a == b)  # Output: False
print('\n Is {a != b} : ', a != b)  # Output: True
print('\n Is {a < b} : ', a < b)   # Output: True
print('\n Is {a > b} : ', a > b)   # Output: False
print('\n Is {a <= b} : ', a <= b)  # Output: True
print('\n Is {a >= b} : ', a >= b)  # Output: False



