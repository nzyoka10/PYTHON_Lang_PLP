# simple python calculator

num1 = int(input('Enter the first number: '))
num2 = int(input('Enter the second number: '))

sum = (num1 + num2)
subtract = (num1 - num2)
multiply = (num1 * num2)
division = (num1 / num2)

print("Sum of num1 and num2: ", sum, "\n")
print("Minus: ", subtract)
print('Multiply: ', multiply)
print('Divide', division)


